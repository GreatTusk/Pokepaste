package modelo;


public class PokemonType   {

    
    private int id;
    private String name;
   

    public PokemonType() {
        this.id = 0;
    }

    public PokemonType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
    
}
